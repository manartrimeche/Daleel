def format_example_as_chat(example: dict) -> dict:
    """Convert a training pair to chat format for tokenizer."""
    inp = example["input"]
    context_parts = []
    for ctx in inp.get("legal_context", []):
        context_parts.append(f"- {ctx['article_ref']}: {ctx['text']}")
    context_block = "\n".join(context_parts) if context_parts else "لا يوجد سياق قانوني متوفر."

    user_msg = f"السياق القانوني:\n{context_block}\n\nسؤال المستخدم: {inp['user_question']}"

    return {
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPT_AR},
            {"role": "user", "content": user_msg},
            {"role": "assistant", "content": example["output"]},
        ]
    }


def build_dataset(args: argparse.Namespace) -> None:
    """Build Arabic/Derja training dataset."""
    all_examples = list(DERJA_PAIRS)

    random.seed(42)
    random.shuffle(all_examples)

    # Split: 80% train, 20% eval
    split_idx = max(1, int(len(all_examples) * 0.8))
    train_data = all_examples[:split_idx]
    eval_data = all_examples[split_idx:]

    logger.info("Dataset built: %d train, %d eval", len(train_data), len(eval_data))

    args.output.parent.mkdir(parents=True, exist_ok=True)
    with args.output.open("w", encoding="utf-8") as f:
        for ex in train_data:
            f.write(json.dumps(ex, ensure_ascii=False) + "\n")

    if args.eval_output:
        with args.eval_output.open("w", encoding="utf-8") as f:
            for ex in eval_data:
                f.write(json.dumps(ex, ensure_ascii=False) + "\n")

    logger.info("Written: %s (%d) — %s (%d)",
                args.output, len(train_data),
                args.eval_output, len(eval_data))


